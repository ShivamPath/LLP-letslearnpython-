#comparison operators : these operators returns boolean
b=4>7
print(b) #result: False

a=7>4
print(a) #result: True

c=92<5
print(c) #result: False

d=14>=7 #greater than or equal to ( either of them stand true then it will print true)
print(d) #result: True 

e=8<=8
print(e) # result : True 

f=15==14
print(f) # result: False

g=15!=14 
print(g) # result: True

